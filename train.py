def train(config, trial=None):
    """Main training function."""
    print("=" * 60)
    print("GCN-LSTM Training")
    print("=" * 60)

    if config.get('seed') is not None:
        set_global_seed(config['seed'], deterministic=config.get('deterministic', False))
        print(f"Seed: {config['seed']} (deterministic={config.get('deterministic', False)})")

    device = config['device']
    print(f"\nDevice: {device}")

    # Load data
    print("\n[1/6] Loading data...")
    X, Y, adj, Z, Y_aux = load_data(config)
    adj = torch.FloatTensor(adj).to(device)

    # Update config with actual dimensions
    config['input_dim'] = X.shape[-1]
    config['num_nodes'] = X.shape[2]

    # When an extra feature is inserted at index 17, wind one-hot shifts by one.
    if config.get('use_pm25_delta', False) or config.get('use_holiday_feature', False):
        config['wind_dir_start_idx'] = 18
        config['wind_dir_end_idx'] = 34

    # Split data BEFORE scaling to prevent data leakage
    print("\n[2/6] Splitting data...")
    train_data, val_data, test_data = split_data(X, Y, config)
    X_train, Y_train = train_data
    X_val, Y_val = val_data
    X_test, Y_test = test_data

    # Split Z and Y_aux along the same chronological boundaries
    n = len(X)
    train_end = int(n * config['train_ratio'])
    val_end = int(n * (config['train_ratio'] + config['val_ratio']))

    if Z is not None:
        Z_train, Z_val, Z_test = Z[:train_end], Z[train_end:val_end], Z[val_end:]
        print(f"  Z split: train={len(Z_train)}, val={len(Z_val)}, test={len(Z_test)}")
    else:
        Z_train = Z_val = Z_test = None

    if Y_aux is not None:
        Y_aux_train = Y_aux[:train_end]
        Y_aux_val   = Y_aux[train_end:val_end]
        Y_aux_test  = Y_aux[val_end:]
        print(f"  Y_aux split: train={len(Y_aux_train)}, val={len(Y_aux_val)}, test={len(Y_aux_test)}")
    else:
        Y_aux_train = Y_aux_val = Y_aux_test = None

    # Correlation-based static adjacency: computed from raw training PM2.5 (no leakage).
    # Replaces Gaussian distance decay as the static component; wind component unchanged.
    if config.get('use_correlation_adj', False):
        N = config['num_nodes']
        pm25_flat = X_train[:, :, :, 0].reshape(-1, N)          # (samples*seq_len, N)
        corr = np.corrcoef(pm25_flat.T).astype(np.float32)       # (N, N)
        corr = np.clip(corr, 0, None)                            # drop negative correlations
        row_sum = corr.sum(axis=1, keepdims=True)
        row_sum[row_sum == 0] = 1.0
        corr_norm = corr / row_sum                               # row-normalised
        config['_corr_adj'] = torch.FloatTensor(corr_norm).to(device)
        off_diag = (corr.sum() - np.trace(corr)) / (N * (N - 1))
        print(f"  Correlation adjacency built: mean off-diag={off_diag:.4f}  "
              f"range=[{corr.min():.3f}, {corr.max():.3f}]")

    # Fit scalers on training data only (prevents data leakage)
    print("\n[3/6] Fitting scalers on training data only...")
    feature_scaler, target_scaler, already_scaled = fit_scalers_on_train(X_train, Y_train, config)

    if already_scaled:
        # Data is already scaled, use as-is
        X_train_scaled, Y_train_scaled = X_train.astype(np.float32), Y_train.astype(np.float32)
        X_val_scaled, Y_val_scaled = X_val.astype(np.float32), Y_val.astype(np.float32)
        X_test_scaled, Y_test_scaled = X_test.astype(np.float32), Y_test.astype(np.float32)
        Z_train_scaled = Z_val_scaled = Z_test_scaled = None
        Y_aux_train_scaled = Y_aux_val_scaled = Y_aux_test_scaled = None

        # Try to load existing target scaler for metrics inverse transform
        scaler_path = os.path.join(config['data_path'], 'target_scaler.save')
        if os.path.exists(scaler_path):
            target_scaler = joblib.load(scaler_path)
            print(f"  Loaded existing target scaler from {scaler_path}")
    else:
        # Scale all splits using training-fitted scalers
        X_train_scaled, Y_train_scaled = scale_data(X_train, Y_train, feature_scaler, target_scaler, config)
        X_val_scaled, Y_val_scaled = scale_data(X_val, Y_val, feature_scaler, target_scaler, config)
        X_test_scaled, Y_test_scaled = scale_data(X_test, Y_test, feature_scaler, target_scaler, config)

        # Scale future met features using the same training-fitted feature_scaler
        if Z_train is not None:
            Z_train_scaled = scale_future_met(Z_train, feature_scaler, config)
            Z_val_scaled   = scale_future_met(Z_val,   feature_scaler, config)
            Z_test_scaled  = scale_future_met(Z_test,  feature_scaler, config)
            print(f"  Z (future met) scaled: shape {Z_train_scaled.shape}, "
                  f"range [{Z_train_scaled[:,:,:,:5].min():.3f}, {Z_train_scaled[:,:,:,:5].max():.3f}] (met features)")
        else:
            Z_train_scaled = Z_val_scaled = Z_test_scaled = None

        # Scale auxiliary pollutant targets using training feature_scaler stats for features 1-5
        if Y_aux_train is not None:
            Y_aux_train_scaled = scale_aux_targets(Y_aux_train, feature_scaler, config)
            Y_aux_val_scaled   = scale_aux_targets(Y_aux_val,   feature_scaler, config)
            Y_aux_test_scaled  = scale_aux_targets(Y_aux_test,  feature_scaler, config)
            print(f"  Y_aux scaled: shape {Y_aux_train_scaled.shape}, "
                  f"range [{Y_aux_train_scaled.min():.3f}, {Y_aux_train_scaled.max():.3f}]")
        else:
            Y_aux_train_scaled = Y_aux_val_scaled = Y_aux_test_scaled = None

        print(f"  Feature scaler mean[:3]: {feature_scaler.mean_[:3]}  std[:3]: {feature_scaler.scale_[:3]}")
        if isinstance(target_scaler, list):
            means = [sc.mean_[0] for sc in target_scaler]
            stds  = [sc.scale_[0] for sc in target_scaler]
            print(f"  Target scalers (per-station): mean [{min(means):.4f}, {max(means):.4f}]  "
                  f"std [{min(stds):.4f}, {max(stds):.4f}]")
        else:
            print(f"  Target scaler mean: {target_scaler.mean_[0]:.4f}  std: {target_scaler.scale_[0]:.4f}")

        # Save scalers for inference
        os.makedirs(config['data_path'], exist_ok=True)
        joblib.dump(target_scaler, os.path.join(config['data_path'], 'target_scaler.save'))
        joblib.dump(feature_scaler, os.path.join(config['data_path'], 'feature_scaler.save'))

        # Pre-compute residual conversion tensors for per-station norm (used in train/val/test loops)
        if config.get('use_per_station_norm', False):
            config['_feat_pm25_mean']  = torch.tensor(float(feature_scaler.mean_[0]),  dtype=torch.float32, device=device)
            config['_feat_pm25_scale'] = torch.tensor(float(feature_scaler.scale_[0]), dtype=torch.float32, device=device)
            config['_target_means']  = torch.tensor([sc.mean_[0]  for sc in target_scaler], dtype=torch.float32, device=device)
            config['_target_scales'] = torch.tensor([sc.scale_[0] for sc in target_scaler], dtype=torch.float32, device=device)

    # Derive EVT threshold from SCALED training targets to match loss computation
    if config.get('evt_threshold') is None:
        q = config['evt_tail_quantile']
        mode = config.get('evt_threshold_mode', 'global')
        if mode == 'global':
            config['evt_threshold'] = float(np.quantile(Y_train_scaled, q))
        elif mode == 'per_node':
            # Y_train_scaled: (samples, horizon, nodes) -> per-node threshold over (samples, horizon)
            config['evt_threshold'] = np.quantile(Y_train_scaled, q, axis=(0, 1)).astype(np.float32)
        else:
            raise ValueError(f"Unknown evt_threshold_mode: {mode}")

    # When using predicted met: pre-train MeteorologicalForecaster on oracle Z,
    # then pass it as a frozen Z-generator during PM2.5 training.
    # Z is excluded from the PM2.5 DataLoader — the forecaster generates it per batch.
    met_forecaster = None
    if config.get('met_forecast_mode') == 'predicted':
        if Z_train_scaled is None:
            raise ValueError(
                "met_forecast_mode='predicted' requires Z (future met oracle) for pre-training. "
                "Set use_future_met=True and generate Z_24.npy first."
            )
        met_forecaster = MeteorologicalForecaster(
            met_dim=len(config['future_met_indices']),
            hidden_dim=config.get('met_hidden_dim', 64),
            num_nodes=config['num_nodes'],
            horizon=config['horizon'],
            num_tf_layers=config.get('met_tf_layers', 2),
            num_heads=config.get('met_num_heads', 4),
            dropout=config['dropout'],
            gat_version=config.get('gat_version', 'v1'),
        ).to(device)
        met_forecaster = pretrain_met_forecaster(
            met_forecaster,
            X_train_scaled, Z_train_scaled,
            X_val_scaled,   Z_val_scaled,
            config, device,
        )
        # Met forecaster is now frozen — PM2.5 DataLoader carries no Z
        Z_dl_train = Z_dl_val = Z_dl_test = None
    elif config.get('met_forecast_mode') == 'persistence':
        # Persistence generates Z from X per batch — no need to carry oracle Z in DataLoader
        Z_dl_train = Z_dl_val = Z_dl_test = None
    else:
        Z_dl_train, Z_dl_val, Z_dl_test = Z_train_scaled, Z_val_scaled, Z_test_scaled

    # Create dataloaders: order is [X, Y, Z (opt), Y_aux (opt)]
    # Y_aux is always at position 3 (after Z slot, even if Z is None — create_dataloaders handles it)
    train_loader, val_loader, test_loader = create_dataloaders(
        (X_train_scaled, Y_train_scaled, Z_dl_train, Y_aux_train_scaled),
        (X_val_scaled,   Y_val_scaled,   Z_dl_val,   Y_aux_val_scaled),
        (X_test_scaled,  Y_test_scaled,  Z_dl_test,  Y_aux_test_scaled),
        config
    )

    # Create model
    print("\n[4/6] Creating model...")
    model_type = config.get('model_type', 'gcn_lstm')
    future_met_dim = len(config['future_met_indices']) if config.get('use_future_met') else 0
    if model_type == 'graph_transformer':
        model = GraphTransformerModel(
            input_dim=config['input_dim'],
            hidden_dim=config['hidden_dim'],
            output_dim=config['output_dim'],
            num_nodes=config['num_nodes'],
            num_tf_layers=config.get('num_tf_layers', 2),
            num_heads=config['num_heads'],
            dropout=config['dropout'],
            horizon=config['horizon'],
            use_node_embeddings=config.get('use_node_embeddings', True),
            use_learnable_alpha_gate=config.get('use_learnable_alpha_gate', False),
            initial_wind_alpha=config.get('wind_alpha', 0.6),
            graph_conv=config.get('graph_conv', 'gcn'),
            num_gat_layers=config.get('num_gat_layers', 1),
            gat_version=config.get('gat_version', 'v1'),
            use_post_temporal_gat=config.get('use_post_temporal_gat', False),
            use_temporal_attention_head=config.get('use_temporal_attention_head', False),
            use_t24_residual=config.get('use_t24_residual', False),
            initial_t24_alpha=config.get('initial_t24_alpha', 0.3),
            future_met_dim=future_met_dim,
            use_multiscale_temporal=config.get('use_multiscale_temporal', False),
            local_window=config.get('local_window', 6),
            n_local_layers=config.get('n_local_layers', 1),
            use_horizon_residual_weights=config.get('use_horizon_residual_weights', False),
            use_learnable_static_adj=config.get('use_learnable_static_adj', False),
            initial_distance_sigma=config.get('distance_sigma', 1800.0),
            use_multitask=config.get('use_multitask', False),
            n_aux_targets=config.get('n_aux_targets', 5),
            use_station_horizon_bias=config.get('use_station_horizon_bias', False),
            use_regime_conditioning=config.get('use_regime_conditioning', False),
            use_tcn_branch=config.get('use_tcn_branch', False),
            use_edge_features=config.get('use_edge_features', False),
            use_dual_channel_spatial=config.get('use_dual_channel_spatial', False),
            use_probabilistic_output=config.get('use_probabilistic_output', False),
            use_pm25_spatial_path=config.get('use_pm25_spatial_path', False),
        ).to(device)
        print(f"  Model type: GraphTransformerModel  graph_conv={config.get('graph_conv', 'gcn')}  gat_version={config.get('gat_version', 'v1')}  num_gat_layers={config.get('num_gat_layers', 1)}  post_gat={config.get('use_post_temporal_gat', False)}  temporal_attn_head={config.get('use_temporal_attention_head', False)}")
    else:
        model = GCNLSTMModel(
            input_dim=config['input_dim'],
            hidden_dim=config['hidden_dim'],
            output_dim=config['output_dim'],
            num_nodes=config['num_nodes'],
            num_layers=config['num_layers'],
            num_heads=config['num_heads'],
            dropout=config['dropout'],
            horizon=config['horizon'],
            use_direct_decoding=config.get('use_direct_decoding', False),
            use_learnable_alpha_gate=config.get('use_learnable_alpha_gate', False),
            initial_wind_alpha=config.get('wind_alpha', 0.6),
            graph_conv=config.get('graph_conv', 'gcn'),
        ).to(device)
        print(f"  Model type: GCNLSTMModel  graph_conv={config.get('graph_conv', 'gcn')}")

    print(f"  Model parameters: {model.get_num_params():,}")
    if config.get('use_wind_adjacency', False) and config.get('use_learnable_alpha_gate', False):
        print(f"  Learnable Alpha Gate: ON (initial alpha={config.get('wind_alpha', 0.6):.3f})")
    
    # Loss and optimizer
    loss_type = str(config.get('loss_type', 'mse')).lower()
    horizon_weights = config.get('horizon_loss_weights', None)
    huber_delta = float(config.get('huber_delta', 1.0))

    if loss_type == 'evt_hybrid':
        # Determine initial lambda (may be adjusted by schedule)
        if config.get('evt_use_lambda_schedule', False):
            initial_lambda = config['evt_lambda_schedule']['initial']
        else:
            initial_lambda = config['evt_lambda']

        base_loss_type = str(config.get('evt_base_loss_type', 'mse')).lower()
        criterion = EVTHybridLoss(
            threshold=config['evt_threshold'],
            lambda_tail=initial_lambda,
            xi=config['evt_xi'],
            asymmetric_penalty=config.get('evt_asymmetric_penalty', False),
            under_penalty_multiplier=config.get('evt_under_penalty_multiplier', 2.0),
            horizon_weights=horizon_weights,
            base_loss_type=base_loss_type,
            huber_delta=huber_delta,
        )

        # Print configuration
        thr = config['evt_threshold']
        if isinstance(thr, (list, tuple, np.ndarray)):
            thr_mean = float(np.mean(thr))
            thr_str = f"per_node(mean={thr_mean:.4f})"
        else:
            thr_str = f"{float(thr):.4f}"
        print(
            f"  Loss: EVT Hybrid (base={base_loss_type}, "
            f"delta={huber_delta:.3f}, lambda={initial_lambda}, "
            f"q={config['evt_tail_quantile']}, xi={config['evt_xi']}, "
            f"threshold={thr_str})"
        )
        hw = horizon_weights
        if hw is not None:
            import numpy as _np
            hw_arr = _np.array(hw, dtype=float)
            hw_norm = hw_arr / hw_arr.mean()
            print(f"  Horizon weights (normalized): {[round(w, 3) for w in hw_norm.tolist()]}")

        if config.get('evt_asymmetric_penalty', False):
            print(
                f"  Asymmetric Penalty: ON (under-prediction multiplier={config.get('evt_under_penalty_multiplier', 2.0)}x)"
            )

        if config.get('evt_use_lambda_schedule', False):
            sched = config['evt_lambda_schedule']
            print(
                f"  Adaptive Lambda Schedule: {sched['initial']} -> {sched['mid']} -> {sched['final']} "
                f"(warmup={sched['warmup_epochs']}, mid={sched['mid_epochs']}, {sched['transition']})"
            )
    elif loss_type in {'gaussian_nll', 'gaussian_nll_evt'}:
        if not config.get('use_probabilistic_output', False):
            raise ValueError("Gaussian NLL losses require use_probabilistic_output=True")

        if loss_type == 'gaussian_nll_evt':
            if config.get('evt_use_lambda_schedule', False):
                initial_lambda = config['evt_lambda_schedule']['initial']
            else:
                initial_lambda = config['evt_lambda']
            criterion = GaussianNLLEVTHybridLoss(
                threshold=config['evt_threshold'],
                lambda_tail=initial_lambda,
                xi=config['evt_xi'],
                asymmetric_penalty=config.get('evt_asymmetric_penalty', False),
                under_penalty_multiplier=config.get('evt_under_penalty_multiplier', 2.0),
                horizon_weights=horizon_weights,
                logvar_min=config.get('nll_logvar_min', -6.0),
                logvar_max=config.get('nll_logvar_max', 4.0),
            )
            print(
                f"  Loss: Gaussian NLL + EVT tail MSE "
                f"(lambda={initial_lambda}, q={config['evt_tail_quantile']}, xi={config['evt_xi']}, "
                f"logvar=[{config.get('nll_logvar_min', -6.0)}, {config.get('nll_logvar_max', 4.0)}])"
            )
        else:
            criterion = GaussianNLLForecastLoss(
                logvar_min=config.get('nll_logvar_min', -6.0),
                logvar_max=config.get('nll_logvar_max', 4.0),
                horizon_weights=horizon_weights,
            )
            print(
                f"  Loss: Gaussian NLL "
                f"(logvar=[{config.get('nll_logvar_min', -6.0)}, {config.get('nll_logvar_max', 4.0)}])"
            )

        if horizon_weights is not None:
            import numpy as _np
            hw_arr = _np.array(horizon_weights, dtype=float)
            hw_norm = hw_arr / hw_arr.mean()
            print(f"  Horizon weights (normalized): {[round(w, 3) for w in hw_norm.tolist()]}")
    elif loss_type in {'mse', 'huber'}:
        criterion = BaseForecastLoss(
            loss_type=loss_type,
            huber_delta=huber_delta,
            horizon_weights=horizon_weights,
        )
        if loss_type == 'huber':
            print(f"  Loss: Huber (delta={huber_delta:.3f})")
        else:
            print("  Loss: MSE")

        if horizon_weights is not None:
            import numpy as _np
            hw_arr = _np.array(horizon_weights, dtype=float)
            hw_norm = hw_arr / hw_arr.mean()
            print(f"  Horizon weights (normalized): {[round(w, 3) for w in hw_norm.tolist()]}")
    else:
        raise ValueError(f"Unsupported loss_type: {loss_type}")

    optimizer = create_optimizer(model.parameters(), config)
    print(f"  Optimizer: {str(config.get('optimizer_type', 'adam')).upper()}")
    
    # Learning rate scheduler: cosine annealing with linear warmup, or ReduceLROnPlateau
    if config.get('use_cosine_schedule', False):
        warmup = config.get('cosine_warmup_epochs', 5)
        total  = config['epochs']
        eta_min_ratio = config.get('cosine_min_lr_ratio', 0.05)
        def _cosine_lr_lambda(epoch):
            if epoch < warmup:
                return float(epoch + 1) / warmup
            progress = (epoch - warmup) / max(total - warmup, 1)
            return eta_min_ratio + (1.0 - eta_min_ratio) * 0.5 * (1.0 + math.cos(math.pi * progress))
        scheduler = optim.lr_scheduler.LambdaLR(optimizer, lr_lambda=_cosine_lr_lambda)
    else:
        scheduler = optim.lr_scheduler.ReduceLROnPlateau(
            optimizer, mode='min', factor=0.5, patience=5
        )
    
    # Training loop
    print("\n[5/6] Training...")
    print("-" * 60)
    
    best_val_loss = float('inf')   # used only for LR scheduler
    best_val_mae = float('inf')    # used for early stopping and checkpoint selection
    patience_counter = 0
    history = {'train_loss': [], 'val_loss': [], 'val_mae': []}
    start_epoch = 0
    
    save_checkpoints = config.get('save_checkpoints', True)
    evaluate_test = config.get('evaluate_test', True)
    save_history = config.get('save_history', True)
    best_state_dict = None

    # Create checkpoint directory if needed
    if save_checkpoints:
        os.makedirs(config['model_save_path'], exist_ok=True)

    # Determine checkpoint filename
    if config.get('use_versioned_checkpoint', False):
        arch_name = config.get('architecture_name', 'model')
        hw_tag = config.get('hardware_tag', 'unknown')
        checkpoint_filename = f"{arch_name}_{hw_tag}_best.pt"
    else:
        checkpoint_filename = config['best_model_name']

    checkpoint_path = os.path.join(config['model_save_path'], checkpoint_filename)
    if save_checkpoints:
        print(f"\nCheckpoint path: {checkpoint_path}")
    else:
        print(f"\nCheckpoint path: {checkpoint_path} (disabled during this run)")

    # Resume from checkpoint if specified
    if save_checkpoints and config.get('resume', False) and os.path.exists(checkpoint_path):
        print(f"\nResuming from checkpoint: {checkpoint_path}")
        checkpoint = torch.load(checkpoint_path, weights_only=False)
        model.load_state_dict(checkpoint['model_state_dict'])
        optimizer.load_state_dict(checkpoint['optimizer_state_dict'])
        start_epoch = checkpoint['epoch'] + 1
        best_val_loss = checkpoint['val_loss']
        best_val_mae = checkpoint.get('val_mae', float('inf'))
        print(f"  Resuming from epoch {start_epoch}")
        print(f"  Best val_loss: {best_val_loss:.6f}, best val_mae: {best_val_mae:.4f}")
    
    for epoch in range(start_epoch, config['epochs']):
        start_time = time.time()

        # Update EVT lambda if using adaptive schedule
        if config.get('loss_type', 'mse') in {'evt_hybrid', 'gaussian_nll_evt'} and config.get('evt_use_lambda_schedule', False):
            new_lambda = get_evt_lambda_for_epoch(
                epoch,
                config['evt_lambda_schedule'],
                config['epochs']
            )
            criterion.set_lambda(new_lambda)

            # Log lambda changes (only when it actually changes)
            if epoch == 0 or epoch == config['evt_lambda_schedule'].get('warmup_epochs', 20) or \
               epoch == config['evt_lambda_schedule'].get('mid_epochs', 40):
                print(f"  -> EVT lambda updated to {new_lambda:.4f} at epoch {epoch+1}")

        # Calculate teacher forcing ratio (linear decay)
        tf_ratio = config['teacher_forcing_start'] - \
                   (config['teacher_forcing_start'] - config['teacher_forcing_end']) * \
                   (epoch / config['epochs'])

        # Train
        train_loss = train_epoch(
            model, train_loader, optimizer, criterion, adj, config, tf_ratio,
            met_forecaster=met_forecaster,
        )

        # Validate — get both loss (for LR scheduler) and MAE (for early stopping)
        val_loss, val_mae = validate(model, val_loader, criterion, adj, config, target_scaler,
                                     met_forecaster=met_forecaster)

        # Step LR scheduler — cosine uses epoch count, ReduceLROnPlateau uses val_mae.
        if config.get('use_cosine_schedule', False):
            scheduler.step()
        else:
            scheduler.step(val_mae)

        # Record history
        history['train_loss'].append(train_loss)
        history['val_loss'].append(val_loss)
        history['val_mae'].append(val_mae)

        elapsed = time.time() - start_time
        alpha_str = ""
        if config.get('use_wind_adjacency', False) and config.get('use_learnable_alpha_gate', False):
            alpha_str = f"Alpha: {float(model.get_wind_alpha().detach().cpu()):.3f} | "
        if config.get('use_t24_residual', False):
            alpha_str += f"T24: {float(model.get_t24_alpha().detach().cpu()):.3f} | "

        # Display current lambda if using schedule
        lambda_str = ""
        if hasattr(criterion, 'lambda_tail') and config.get('evt_use_lambda_schedule', False):
            lambda_str = f"λ: {criterion.lambda_tail:.3f} | "

        scale_label = "µg/m³" if target_scaler is not None else "norm"
        print(f"Epoch {epoch+1:3d}/{config['epochs']} | "
              f"Train Loss: {train_loss:.6f} | "
              f"Val Loss: {val_loss:.6f} | "
              f"Val MAE: {val_mae:.4f} ({scale_label}) | "
              f"{alpha_str}"
              f"{lambda_str}"
              f"TF: {tf_ratio:.2f} | "
              f"Time: {elapsed:.1f}s")

        # Early stopping and checkpoint selection: use val_mae (loss-function-independent)
        if val_mae < best_val_mae:
            best_val_mae = val_mae
            best_val_loss = val_loss
            patience_counter = 0

            # Keep best weights in memory (needed when checkpoint saving is disabled)
            best_state_dict = {k: v.detach().cpu().clone() for k, v in model.state_dict().items()}

            if save_checkpoints:
                # Save best model with metadata
                torch.save({
                    'epoch': epoch,
                    'model_state_dict': model.state_dict(),
                    'optimizer_state_dict': optimizer.state_dict(),
                    'val_loss': val_loss,
                    'val_mae': val_mae,
                    'train_loss': train_loss,
                    'config': config,
                    'hardware': {
                        'device': str(device),
                        'tag': config.get('hardware_tag', 'unknown'),
                        'cuda_available': torch.cuda.is_available(),
                        'cuda_device_name': torch.cuda.get_device_name(0) if torch.cuda.is_available() else None
                    },
                    'architecture': {
                        'name': config.get('architecture_name', 'unknown'),
                        'num_params': model.get_num_params(),
                        'use_direct_decoding': config.get('use_direct_decoding', False),
                        'use_wind_adjacency': config.get('use_wind_adjacency', False),
                        'use_learnable_alpha_gate': config.get('use_learnable_alpha_gate', False),
                        'learned_wind_alpha': (
                            float(model.get_wind_alpha().detach().cpu())
                            if model.get_wind_alpha() is not None else None
                        )
                    },
                    'timestamp': time.strftime('%Y-%m-%d %H:%M:%S')
                }, checkpoint_path)
                scale_label = "µg/m³" if target_scaler is not None else "norm"
                print(f"  -> Saved best model (val_mae: {val_mae:.4f} {scale_label})")
            else:
                scale_label = "µg/m³" if target_scaler is not None else "norm"
                print(f"  -> New best model in memory (val_mae: {val_mae:.4f} {scale_label})")
        else:
            patience_counter += 1
            if patience_counter >= config['patience']:
                print(f"\nEarly stopping at epoch {epoch+1}")
                break

        # Report intermediate value for Optuna and prune bad trials early
        # Use val_mae so pruning is loss-function-independent
        if trial is not None:
            trial.report(val_mae, step=epoch)
            if trial.should_prune():
                raise RuntimeError("TRIAL_PRUNED")
    
    # Restore best model weights
    if save_checkpoints and os.path.exists(checkpoint_path):
        checkpoint = torch.load(checkpoint_path, weights_only=False)
        model.load_state_dict(checkpoint['model_state_dict'])
    elif best_state_dict is not None:
        model.load_state_dict(best_state_dict)

    metrics = {'best_val_loss': best_val_loss, 'best_val_mae': best_val_mae}
    if evaluate_test:
        print("\n[6/6] Evaluating best model...")
        print("-" * 60)

        # Target scaler was already saved earlier and is in scope
        test_metrics = compute_metrics(model, test_loader, adj, config, target_scaler,
                                       met_forecaster=met_forecaster)
        metrics.update(test_metrics)

        print("\nTest Set Metrics:")
        for name, value in test_metrics.items():
            print(f"  {name}: {value:.4f}")
    else:
        print("\n[6/6] Skipping test evaluation (evaluate_test=False)")

    # Save training history
    if save_history:
        np.save(
            os.path.join(config['model_save_path'], 'history.npy'),
            history
        )
    
    print("\n" + "=" * 60)
    print("Training complete!")
    if save_checkpoints:
        print(f"Best model saved to: {checkpoint_path}")
    else:
        print("Best model checkpoint saving was disabled for this run.")
    print(f"Architecture: {config.get('architecture_name', 'N/A')}")
    print(f"Hardware: {config.get('hardware_tag', 'N/A')}")
    print("=" * 60)

    return model, history, metrics


