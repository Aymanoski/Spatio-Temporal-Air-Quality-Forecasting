def load_data(config):
    """Load preprocessed data tensors.

    Looks for X_{input_len}.npy first (named files generated by utils/window.py
    --input_len flag). Falls back to X.npy for backward compatibility.
    """
    data_path = config['data_path']
    input_len = config['input_len']

    # Prefer named files (e.g. X_48.npy) so multiple window sizes can coexist.
    # Fall back to X.npy if the named file doesn't exist (original behaviour).
    if config.get('use_pm25_delta', False):
        suffix = '_delta'
    elif config.get('use_holiday_feature', False):
        suffix = '_holiday'
    else:
        suffix = ''
    x_named = os.path.join(data_path, f'X_{input_len}{suffix}.npy')
    y_named = os.path.join(data_path, f'Y_{input_len}{suffix}.npy')
    x_default = os.path.join(data_path, 'X.npy')
    y_default = os.path.join(data_path, 'Y.npy')

    if os.path.exists(x_named):
        X = np.load(x_named)
        Y = np.load(y_named)
        print(f"Loaded windowed data (input_len={input_len}): {x_named}")
    else:
        gen_flag = f'--input_len {input_len}' + (' --add_pm25_delta' if suffix else '')
        print(f"[Warning] {x_named} not found — falling back to X.npy. "
              f"Run: python utils/window.py {gen_flag}")
        X = np.load(x_default)
        Y = np.load(y_default)

    adj = np.load(os.path.join(data_path, 'adjacency.npy'))

    print(f"Loaded X: {X.shape}")  # (samples, input_len, num_nodes, features)
    print(f"Loaded Y: {Y.shape}")  # (samples, horizon, num_nodes)
    print(f"Loaded adjacency: {adj.shape}")  # (num_nodes, num_nodes)

    # Load future meteorological features if enabled
    Z = None
    if config.get('use_future_met', False):
        z_path = os.path.join(data_path, f'Z_{input_len}.npy')
        if not os.path.exists(z_path):
            raise FileNotFoundError(
                f"Future met tensor not found: {z_path}\n"
                f"Generate it with: python utils/window.py --input_len {input_len} --future_met"
            )
        Z = np.load(z_path)
        print(f"Loaded Z (future met): {Z.shape}")  # (samples, horizon, num_nodes, F_met)

    # Load auxiliary pollutant targets if multi-task is enabled
    Y_aux = None
    if config.get('use_multitask', False):
        y_aux_path = os.path.join(data_path, f'Y_aux_{input_len}.npy')
        if not os.path.exists(y_aux_path):
            raise FileNotFoundError(
                f"Y_aux tensor not found: {y_aux_path}\n"
                f"Generate it with: python utils/window.py --input_len {input_len} --save_y_aux"
            )
        Y_aux = np.load(y_aux_path)
        print(f"Loaded Y_aux (aux targets): {Y_aux.shape}")  # (samples, horizon, nodes, 5)

    return X, Y, adj, Z, Y_aux


def extract_wind_features(X_batch, config):
    """
    Extract wind speed and direction from input batch.

    Args:
        X_batch: (batch, timesteps, num_nodes, features) tensor
        config: configuration dict

    Returns:
        wind_speeds: (batch, timesteps, num_nodes) tensor
        wind_directions: (batch, timesteps, num_nodes, num_wind_categories) tensor
    """
    wind_speed_idx = config['wind_speed_idx']
    wind_dir_start = config['wind_dir_start_idx']
    wind_dir_end = config['wind_dir_end_idx']

    # Extract wind speed (single feature)
    wind_speeds = X_batch[:, :, :, wind_speed_idx]  # (batch, timesteps, nodes)

    # Extract wind direction one-hot (multiple features)
    wind_directions = X_batch[:, :, :, wind_dir_start:wind_dir_end]  # (batch, timesteps, nodes, categories)

    return wind_speeds, wind_directions


def build_dynamic_adjacency(X_batch, config, device, alpha_override=None, sigma_override=None,
                            static_adj_override=None):
    """
    Build dynamic wind-aware adjacency matrix (or sequence) for a batch.
    Uses GPU-optimized computation when possible.

    Args:
        X_batch: (batch, timesteps, num_nodes, features) tensor
        config: configuration dict
        device: torch device
        alpha_override: learnable alpha tensor from model.get_wind_alpha() — keeps grad flow
        sigma_override: learnable sigma tensor from model.get_distance_sigma() — keeps grad flow
        static_adj_override: (N, N) learnable static adjacency from model.get_static_adj() — keeps grad flow

    Returns:
        adj_batch: (batch, num_nodes, num_nodes)            when use_per_timestep_adj=False
                   (batch, timesteps, num_nodes, num_nodes) when use_per_timestep_adj=True
    """
    # Reconstruct raw wspm (m/s) for the adjacency builder.
    # X_batch carries StandardScaler-normalised wspm; the tanh(x/5) speed-factor and
    # the 0.1 m/s calm threshold are calibrated for raw m/s, not z-scores.
    # The model still receives the scaled value — only the adjacency branch is fixed.
    wspm_idx   = config['wind_speed_idx']
    wspm_mean  = config.get('_wspm_mean',  0.0)
    wspm_scale = config.get('_wspm_scale', 1.0)
    if wspm_scale != 1.0 or wspm_mean != 0.0:
        X_adj = X_batch.clone()
        X_adj[:, :, :, wspm_idx] = (X_batch[:, :, :, wspm_idx] * wspm_scale + wspm_mean).clamp(min=0.0)
    else:
        X_adj = X_batch

    # Per-timestep adjacency: build a separate A_t for each input timestep.
    # Gradients flow through alpha_override if it is a learnable tensor.
    if config.get('use_per_timestep_adj', False):
        return build_per_timestep_adjacency_gpu(
            X_adj, config,
            alpha_override=alpha_override,
            sigma_override=sigma_override,
        )

    # Use the torch implementation whenever any learnable param is provided so
    # gradients can flow from the loss back through the adjacency construction.
    if X_batch.is_cuda or alpha_override is not None or sigma_override is not None or static_adj_override is not None:
        return build_dynamic_adjacency_gpu(
            X_adj, config,
            alpha_override=alpha_override,
            sigma_override=sigma_override,
            static_adj_override=static_adj_override,
        )

    # Fallback to CPU version for non-GPU tensors
    wind_speeds, wind_directions = extract_wind_features(X_adj, config)

    # Build wind-aware adjacency
    adj_batch = build_wind_aware_adjacency_batch(
        wind_speeds=wind_speeds,
        wind_directions=wind_directions,
        wind_categories=WIND_CATEGORIES,
        alpha=config['wind_alpha'] if alpha_override is None else float(alpha_override),
        distance_sigma=config['distance_sigma'],
        aggregation_mode=config.get('wind_aggregation_mode', 'recent_weighted'),
        recency_beta=config.get('wind_recency_beta', 3.0),
        direction_method=config.get('wind_direction_method', 'circular'),
        normalization=config.get('wind_normalization', 'row'),
        calm_speed_threshold=config.get('wind_calm_speed_threshold', 0.1)
    )

    # Convert to tensor
    adj_batch = torch.FloatTensor(adj_batch).to(device)

    return adj_batch


def split_data(X, Y, config):
    """Split data into train/val/test sets (chronological split)."""
    n_samples = len(X)

    train_end = int(n_samples * config['train_ratio'])
    val_end = int(n_samples * (config['train_ratio'] + config['val_ratio']))

    X_train, Y_train = X[:train_end], Y[:train_end]
    X_val, Y_val = X[train_end:val_end], Y[train_end:val_end]
    X_test, Y_test = X[val_end:], Y[val_end:]

    print(f"\nData split:")
    print(f"  Train: {len(X_train)} samples")
    print(f"  Val:   {len(X_val)} samples")
    print(f"  Test:  {len(X_test)} samples")

    return (X_train, Y_train), (X_val, Y_val), (X_test, Y_test)


def inverse_transform_targets(arr, target_scaler):
    """
    Inverse-transform targets.
    target_scaler: single scaler (global) or list of per-station scalers.
    arr shape: (samples, horizon, nodes).
    """
    if isinstance(target_scaler, list):
        out = np.zeros_like(arr)
        for s in range(arr.shape[-1]):
            flat = arr[..., s].reshape(-1, 1)
            out[..., s] = target_scaler[s].inverse_transform(flat).reshape(arr[..., s].shape)
        return out
    orig_shape = arr.shape
    return target_scaler.inverse_transform(arr.reshape(-1, 1)).reshape(orig_shape)


def fit_scalers_on_train(X_train, Y_train, config):
    """
    Fit scalers on training data only to prevent data leakage.

    Args:
        X_train: Training input data (samples, seq_len, nodes, features)
        Y_train: Training target data (samples, horizon, nodes)
        config: Configuration dict

    Returns:
        feature_scaler: Fitted scaler for input features (excluding wind one-hot)
        target_scaler: Fitted scaler for PM2.5 targets
        already_scaled: Boolean indicating if data appears pre-scaled
    """
    # Get dimensions
    n_samples, seq_len, n_nodes, n_features = X_train.shape

    # Find wind direction feature indices (these are one-hot, don't scale)
    wind_start_idx = config.get('wind_dir_start_idx', 17)

    # Check if data appears already scaled (values mostly in 0-1 range)
    # PM2.5 (index 0) in original scale is typically 0-500+ µg/m³
    pm25_max = X_train[:, :, :, 0].max()
    pm25_min = X_train[:, :, :, 0].min()
    y_max = Y_train.max()
    y_min = Y_train.min()

    # If PM2.5 values are in 0-1 range, data is likely pre-scaled
    already_scaled = pm25_max <= 1.5 and pm25_min >= -0.5 and y_max <= 1.5 and y_min >= -0.5

    if already_scaled:
        print("  [WARNING] Data appears to be pre-scaled (values in 0-1 range).")
        print("  Skipping additional scaling to avoid double-scaling.")
        print("  For proper data leakage prevention, regenerate data:")
        print("    1. python preproccess.py")
        print("    2. cd utils && python window.py")
        return None, None, True

    # Apply log1p to skewed input features and PM2.5 target before fitting scalers.
    if config.get('use_log_transform', False):
        X_train_fit = X_train.copy()
        for idx in config.get('log_transform_indices', [0]):
            X_train_fit[:, :, :, idx] = np.log1p(X_train[:, :, :, idx])
        Y_train_fit = np.log1p(Y_train)
    else:
        X_train_fit = X_train
        Y_train_fit = Y_train

    # Flatten X for fitting: (samples * seq_len * nodes, features)
    X_flat = X_train_fit.reshape(-1, n_features)

    # Fit feature scaler on non-wind features (indices 0 to wind_start_idx)
    # This includes PM2.5 (idx 0), other pollutants, meteo, and temporal features
    feature_scaler = StandardScaler()
    feature_scaler.fit(X_flat[:, :wind_start_idx])

    # Store wspm inverse-transform stats so build_dynamic_adjacency can recover raw m/s.
    wspm_idx = config.get('wind_speed_idx', 10)
    config['_wspm_mean']  = float(feature_scaler.mean_[wspm_idx])
    config['_wspm_scale'] = float(feature_scaler.scale_[wspm_idx])

    # Fit target scaler(s) on Y values (PM2.5 only)
    if config.get('use_per_station_norm', False):
        target_scaler = []
        for s in range(n_nodes):
            sc = StandardScaler()
            sc.fit(Y_train_fit[:, :, s].reshape(-1, 1))
            target_scaler.append(sc)
        means = [sc.mean_[0] for sc in target_scaler]
        stds  = [sc.scale_[0] for sc in target_scaler]
        print(f"  Per-station target scalers: mean range [{min(means):.4f}, {max(means):.4f}]  "
              f"std range [{min(stds):.4f}, {max(stds):.4f}]")
    else:
        Y_flat = Y_train_fit.reshape(-1, 1)
        target_scaler = StandardScaler()
        target_scaler.fit(Y_flat)

    return feature_scaler, target_scaler, False


def scale_data(X, Y, feature_scaler, target_scaler, config):
    """
    Scale input features and targets using pre-fitted scalers.

    Args:
        X: Input data (samples, seq_len, nodes, features)
        Y: Target data (samples, horizon, nodes)
        feature_scaler: Pre-fitted scaler for features
        target_scaler: Pre-fitted scaler for targets
        config: Configuration dict

    Returns:
        X_scaled, Y_scaled: Scaled arrays
    """
    n_samples, seq_len, n_nodes, n_features = X.shape
    wind_start_idx = config.get('wind_dir_start_idx', 17)

    # Apply log1p to skewed input features — must match fit_scalers_on_train.
    if config.get('use_log_transform', False):
        X_to_scale = X.copy()
        for idx in config.get('log_transform_indices', [0]):
            X_to_scale[:, :, :, idx] = np.log1p(X[:, :, :, idx])
        Y_to_scale = np.log1p(Y)
    else:
        X_to_scale = X
        Y_to_scale = Y

    # Scale features
    X_flat = X_to_scale.reshape(-1, n_features)
    X_scaled_features = feature_scaler.transform(X_flat[:, :wind_start_idx])

    # Reconstruct X with scaled features + unscaled wind one-hot
    X_scaled_flat = np.concatenate([
        X_scaled_features,
        X_flat[:, wind_start_idx:]  # Wind one-hot stays unchanged
    ], axis=1)
    X_scaled = X_scaled_flat.reshape(n_samples, seq_len, n_nodes, n_features)

    # Scale targets — per-station or global
    if isinstance(target_scaler, list):
        n_s, h_s, n_n = Y_to_scale.shape
        Y_scaled = np.zeros_like(Y_to_scale)
        for s in range(n_n):
            Y_scaled[:, :, s] = target_scaler[s].transform(
                Y_to_scale[:, :, s].reshape(-1, 1)
            ).reshape(n_s, h_s)
    else:
        Y_flat = Y_to_scale.reshape(-1, 1)
        Y_scaled = target_scaler.transform(Y_flat).reshape(Y.shape)

    return X_scaled.astype(np.float32), Y_scaled.astype(np.float32)


def scale_future_met(Z, feature_scaler, config):
    """
    Scale future meteorological features using the training-fitted feature scaler.

    Z contains: temp(6), pres(7), dewp(8), rain(9), wspm(10) + wind direction one-hot (17-32).
    The first 5 features (indices 0-4 within Z) correspond to original feature indices 6-10,
    which are columns 6-10 in the feature_scaler (fitted on original features 0-16).
    Wind direction (indices 5-20 within Z) is one-hot — no scaling needed.
    """
    n_samples, h, n_nodes, f_met = Z.shape
    Z_flat = Z.reshape(-1, f_met)

    # Reconstruct a dummy array matching the feature_scaler's input shape (17 features: 0-16)
    # Place the 5 met features at their original positions (columns 6-10)
    n_scaler_cols = feature_scaler.n_features_in_   # 17
    dummy = np.zeros((len(Z_flat), n_scaler_cols), dtype=np.float32)
    dummy[:, 6:11] = Z_flat[:, :5]                  # temp, pres, dewp, rain, wspm

    # Keep future met scaling aligned with X scaling when any of these original
    # feature columns use log1p (notably wspm at feature index 10).
    if config.get('use_log_transform', False):
        for idx in config.get('log_transform_indices', [0]):
            if 0 <= idx < n_scaler_cols:
                dummy[:, idx] = np.log1p(dummy[:, idx])

    dummy_scaled = feature_scaler.transform(dummy)

    # Reconstruct scaled Z: scaled met features + unchanged wind direction one-hot
    Z_scaled_flat = np.concatenate([dummy_scaled[:, 6:11], Z_flat[:, 5:]], axis=1)
    return Z_scaled_flat.reshape(n_samples, h, n_nodes, f_met).astype(np.float32)


def scale_aux_targets(Y_aux, feature_scaler, config):
    """
    Scale Y_aux (future PM10, SO2, NO2, CO, O3) using the training feature_scaler.

    Y_aux shape: (N_samples, horizon, num_nodes, 5)
    Features 1-5 in original space correspond to indices 1-5 in feature_scaler.mean_/scale_.
    Applies log1p first (same indices are in log_transform_indices), then StandardScaler stats.
    """
    aux_indices = config.get('aux_target_indices', [1, 2, 3, 4, 5])  # feature indices in X
    log_indices = set(config.get('log_transform_indices', [])) if config.get('use_log_transform') else set()

    Y_aux_out = Y_aux.copy().astype(np.float32)
    for j, feat_idx in enumerate(aux_indices):
        if feat_idx in log_indices:
            Y_aux_out[..., j] = np.log1p(Y_aux[..., j])

    # Apply StandardScaler stats: feature_scaler was fit on features 0..wind_start_idx-1
    # aux_indices are within that range, so feature_scaler.mean_/scale_ are directly indexable.
    mean = feature_scaler.mean_[aux_indices]    # (5,)
    scale = feature_scaler.scale_[aux_indices]  # (5,)
    Y_aux_out = (Y_aux_out - mean) / scale
    return Y_aux_out.astype(np.float32)


def create_dataloaders(train_data, val_data, test_data, config):
    """Create PyTorch DataLoaders."""
    X_train, Y_train = train_data[0], train_data[1]
    X_val,   Y_val   = val_data[0],   val_data[1]
    X_test,  Y_test  = test_data[0],  test_data[1]
    Z_train = train_data[2] if len(train_data) > 2 else None
    Z_val   = val_data[2]   if len(val_data)   > 2 else None
    Z_test  = test_data[2]  if len(test_data)  > 2 else None
    # Y_aux always at position 3 (after Z, even if Z is None — caller must maintain ordering)
    Y_aux_train = train_data[3] if len(train_data) > 3 else None
    Y_aux_val   = val_data[3]   if len(val_data)   > 3 else None
    Y_aux_test  = test_data[3]  if len(test_data)  > 3 else None

    # Tensor order in batch: [X, Y, Z (opt), Y_aux (opt)]
    # train_epoch/validate extract by position using config flags.
    def _make_dataset(X, Y, Z, Y_aux):
        tensors = [torch.FloatTensor(X), torch.FloatTensor(Y)]
        if Z is not None:
            tensors.append(torch.FloatTensor(Z))
        if Y_aux is not None:
            tensors.append(torch.FloatTensor(Y_aux))
        return TensorDataset(*tensors)

    train_dataset = _make_dataset(X_train, Y_train, Z_train, Y_aux_train)
    val_dataset   = _make_dataset(X_val,   Y_val,   Z_val,   Y_aux_val)
    test_dataset  = _make_dataset(X_test,  Y_test,  Z_test,  Y_aux_test)
    
    train_loader = DataLoader(
        train_dataset,
        batch_size=config['batch_size'],
        shuffle=True,
        drop_last=True
    )
    val_loader = DataLoader(
        val_dataset,
        batch_size=config['batch_size'],
        shuffle=False
    )
    test_loader = DataLoader(
        test_dataset,
        batch_size=config['batch_size'],
        shuffle=False
    )
    
    return train_loader, val_loader, test_loader


