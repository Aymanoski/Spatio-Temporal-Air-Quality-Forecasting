def compute_base_regression_loss(predictions, targets, loss_type='mse', huber_delta=1.0,
                                 horizon_weights=None):
    """
    Compute the primary regression loss in normalized space.

    Supports plain MSE or Huber/SmoothL1, with optional per-horizon reweighting.
    """
    if loss_type == 'mse':
        per_element = (predictions - targets) ** 2
    elif loss_type == 'huber':
        per_element = F.smooth_l1_loss(
            predictions,
            targets,
            beta=float(huber_delta),
            reduction='none',
        )
    else:
        raise ValueError(f"Unsupported base loss_type: {loss_type}")

    if horizon_weights is not None:
        horizon_weights = horizon_weights.to(device=predictions.device, dtype=predictions.dtype)
        horizon_count = predictions.shape[1]
        weight_shape = [1] * per_element.ndim
        weight_shape[1] = horizon_count
        per_element = per_element * horizon_weights[:horizon_count].view(*weight_shape)

    return per_element.mean()


class BaseForecastLoss(nn.Module):
    """Primary regression loss used for training in normalized space."""

    def __init__(self, loss_type='mse', huber_delta=1.0, horizon_weights=None):
        super().__init__()
        self.loss_type = str(loss_type).lower()
        self.huber_delta = float(huber_delta)
        if self.loss_type == 'huber' and self.huber_delta <= 0:
            raise ValueError("huber_delta must be > 0 for Huber loss")

        if horizon_weights is not None:
            hw = torch.as_tensor(horizon_weights, dtype=torch.float32)
            hw = hw / hw.mean()   # normalize: preserves overall loss scale
            self.register_buffer("horizon_weights", hw)
        else:
            self.register_buffer("horizon_weights", None)

    def forward(self, predictions, targets):
        return compute_base_regression_loss(
            predictions,
            targets,
            loss_type=self.loss_type,
            huber_delta=self.huber_delta,
            horizon_weights=self.horizon_weights,
        )


class EVTHybridLoss(nn.Module):
    """
    Hybrid loss: primary regression loss (MSE or Huber) + EVT tail-aware penalty.

    Penalizes errors more heavily for extreme values above the threshold.
    Uses a power-law penalty inspired by GPD to emphasize extreme value errors.

    Improvements:
    - Asymmetric penalty: under-predictions penalized more than over-predictions
    - Adaptive lambda: can be adjusted during training via set_lambda()
    """

    def __init__(self, threshold, lambda_tail=0.05, xi=0.10, eps=1e-6,
                 asymmetric_penalty=False, under_penalty_multiplier=2.0,
                 horizon_weights=None, base_loss_type='mse', huber_delta=1.0):
        super().__init__()
        # threshold can be a float (global) or a 1D array-like (per node).
        thr = torch.as_tensor(threshold, dtype=torch.float32)
        self.register_buffer("threshold", thr)
        self.lambda_tail = float(lambda_tail)
        self.xi = float(xi)
        self.eps = float(eps)
        self.asymmetric_penalty = asymmetric_penalty
        self.under_penalty_multiplier = float(under_penalty_multiplier)
        self.base_loss = BaseForecastLoss(
            loss_type=base_loss_type,
            huber_delta=huber_delta,
            horizon_weights=horizon_weights,
        )

    def set_lambda(self, new_lambda):
        """Update lambda weight (for adaptive scheduling)."""
        self.lambda_tail = float(new_lambda)

    def forward(self, predictions, targets):
        # Base regression loss — optionally weighted per horizon.
        # predictions/targets: (B, H, N)
        base_loss = self.base_loss(predictions, targets)

        # Identify extreme values (above threshold)
        thr = self.threshold.to(device=targets.device, dtype=targets.dtype)
        if thr.ndim == 0:
            thr_view = thr
        elif thr.ndim == 1:
            # Per-node thresholds; predictions/targets are (batch, horizon, nodes)
            thr_view = thr.view(1, 1, -1)
        else:
            raise ValueError(f"Unsupported threshold shape: {tuple(thr.shape)}")

        mask = targets > thr_view

        if mask.any():
            # Get predictions and targets for extreme values
            extreme_preds = predictions[mask]
            extreme_targets = targets[mask]

            # Select matching thresholds for masked elements (handles global or per-node thresholds)
            thr_selected = thr_view.expand_as(targets)[mask]
            target_excess = extreme_targets - thr_selected

            # Weighted squared error directly on predictions (keeps gradient for under-prediction)
            err = extreme_preds - extreme_targets

            # Weight increases with magnitude of excess (stabilize denominator)
            mean_excess = target_excess.mean().detach()
            excess_weight = 1.0 + self.xi * target_excess / (mean_excess + self.eps)
            excess_weight = torch.clamp(excess_weight, min=1.0)

            # Asymmetric penalty: penalize under-prediction more
            if self.asymmetric_penalty:
                under_mask = err < 0
                excess_weight = excess_weight * torch.where(
                    under_mask,
                    err.new_tensor(self.under_penalty_multiplier),
                    err.new_tensor(1.0)
                )

            tail_loss = (excess_weight * (err ** 2)).mean()
        else:
            tail_loss = torch.zeros(1, device=predictions.device, dtype=predictions.dtype)

        # Combine primary regression loss + weighted tail penalty
        return base_loss + self.lambda_tail * tail_loss


def compute_evt_tail_loss(predictions, targets, threshold, xi=0.10, eps=1e-6,
                          asymmetric_penalty=False, under_penalty_multiplier=2.0):
    """Tail weighted squared error used by EVT hybrid objectives."""
    thr = torch.as_tensor(threshold, dtype=targets.dtype, device=targets.device)
    if thr.ndim == 0:
        thr_view = thr
    elif thr.ndim == 1:
        thr_view = thr.view(1, 1, -1)
    else:
        raise ValueError(f"Unsupported threshold shape: {tuple(thr.shape)}")

    mask = targets > thr_view
    if not mask.any():
        return torch.zeros(1, device=predictions.device, dtype=predictions.dtype)

    extreme_preds = predictions[mask]
    extreme_targets = targets[mask]
    thr_selected = thr_view.expand_as(targets)[mask]
    target_excess = extreme_targets - thr_selected
    err = extreme_preds - extreme_targets
    mean_excess = target_excess.mean().detach()
    excess_weight = 1.0 + float(xi) * target_excess / (mean_excess + float(eps))
    excess_weight = torch.clamp(excess_weight, min=1.0)

    if asymmetric_penalty:
        under_mask = err < 0
        excess_weight = excess_weight * torch.where(
            under_mask,
            err.new_tensor(float(under_penalty_multiplier)),
            err.new_tensor(1.0),
        )

    return (excess_weight * (err ** 2)).mean()


class GaussianNLLForecastLoss(nn.Module):
    """Gaussian negative log likelihood for mean/log-variance forecasts."""

    requires_log_var = True

    def __init__(self, logvar_min=-6.0, logvar_max=4.0, horizon_weights=None):
        super().__init__()
        self.logvar_min = float(logvar_min)
        self.logvar_max = float(logvar_max)
        if horizon_weights is not None:
            hw = torch.as_tensor(horizon_weights, dtype=torch.float32)
            hw = hw / hw.mean()
            self.register_buffer("horizon_weights", hw)
        else:
            self.register_buffer("horizon_weights", None)

    def forward(self, predictions, targets, log_vars):
        log_vars = torch.clamp(log_vars, min=self.logvar_min, max=self.logvar_max)
        per_element = 0.5 * (torch.exp(-log_vars) * (predictions - targets) ** 2 + log_vars)

        if self.horizon_weights is not None:
            horizon_count = predictions.shape[1]
            weight_shape = [1] * per_element.ndim
            weight_shape[1] = horizon_count
            weights = self.horizon_weights.to(device=predictions.device, dtype=predictions.dtype)
            per_element = per_element * weights[:horizon_count].view(*weight_shape)

        return per_element.mean()


class GaussianNLLEVTHybridLoss(nn.Module):
    """Gaussian NLL for the mean plus the existing EVT tail MSE pressure."""

    requires_log_var = True

    def __init__(self, threshold, lambda_tail=0.05, xi=0.10, eps=1e-6,
                 asymmetric_penalty=False, under_penalty_multiplier=2.0,
                 horizon_weights=None, logvar_min=-6.0, logvar_max=4.0):
        super().__init__()
        self.nll = GaussianNLLForecastLoss(
            logvar_min=logvar_min,
            logvar_max=logvar_max,
            horizon_weights=horizon_weights,
        )
        thr = torch.as_tensor(threshold, dtype=torch.float32)
        self.register_buffer("threshold", thr)
        self.lambda_tail = float(lambda_tail)
        self.xi = float(xi)
        self.eps = float(eps)
        self.asymmetric_penalty = bool(asymmetric_penalty)
        self.under_penalty_multiplier = float(under_penalty_multiplier)

    def set_lambda(self, new_lambda):
        self.lambda_tail = float(new_lambda)

    def forward(self, predictions, targets, log_vars):
        nll_loss = self.nll(predictions, targets, log_vars)
        tail_loss = compute_evt_tail_loss(
            predictions,
            targets,
            self.threshold,
            xi=self.xi,
            eps=self.eps,
            asymmetric_penalty=self.asymmetric_penalty,
            under_penalty_multiplier=self.under_penalty_multiplier,
        )
        return nll_loss + self.lambda_tail * tail_loss


def compute_forecast_loss(criterion, predictions, targets, log_vars=None):
    """Dispatch loss calls for deterministic and probabilistic criteria."""
    if getattr(criterion, "requires_log_var", False):
        if log_vars is None:
            raise ValueError("Probabilistic loss requires log_vars from the model")
        return criterion(predictions, targets, log_vars)
    return criterion(predictions, targets)


class RevIN:
    """
    Per-instance reversible normalization across the T=24 time steps.

    Applied to selected features of X_batch before the model forward pass.
    Statistics are per (sample, node), computed across the input sequence.
    Call normalize() before forward, denormalize() after to recover
    predictions in the original (log1p + StdScaler) space.

    Only feature_indices[0] (PM2.5) is used for denormalization because
    the model output is a single PM2.5 scalar per horizon step.
    """

    def __init__(self, feature_indices=(0,), eps=1e-5):
        self.feature_indices = list(feature_indices)
        self.eps = eps
        self._mean = None  # (B, 1, N, len_f), updated by normalize()
        self._std  = None  # (B, 1, N, len_f), updated by normalize()

    def normalize(self, x):
        """Return a RevIN-normalized clone of x and store per-instance stats.
        Args:
            x: (B, T, N, F) float tensor
        Returns:
            x_normed: (B, T, N, F) with feature_indices instance-normalized
        """
        subset = x[:, :, :, self.feature_indices]           # (B, T, N, len_f)
        self._mean = subset.mean(dim=1, keepdim=True)       # (B, 1, N, len_f)
        self._std  = subset.std(dim=1, keepdim=True).clamp(min=self.eps)
        x_normed = x.clone()
        x_normed[:, :, :, self.feature_indices] = (subset - self._mean) / self._std
        return x_normed

    def denormalize(self, preds):
        """Reverse instance normalization on PM2.5 predictions.
        Args:
            preds: (B, H, N) float tensor in RevIN space
        Returns:
            (B, H, N) in log1p + StdScaler space
        """
        mean = self._mean[:, 0, :, 0]  # (B, N) — PM2.5 stats from last normalize()
        std  = self._std[:,  0, :, 0]  # (B, N)
        return preds * std.unsqueeze(1) + mean.unsqueeze(1)  # (B, H, N)


def create_optimizer(parameters, config, lr=None, weight_decay=None, optimizer_type=None):
    """Create the configured optimizer."""
    optimizer_name = str(optimizer_type or config.get('optimizer_type', 'adam')).lower()
    lr_value = float(config['learning_rate'] if lr is None else lr)
    weight_decay_value = (
        float(config.get('weight_decay', 0.0))
        if weight_decay is None else float(weight_decay)
    )

    if optimizer_name == 'adamw':
        return optim.AdamW(parameters, lr=lr_value, weight_decay=weight_decay_value)
    if optimizer_name == 'adam':
        return optim.Adam(parameters, lr=lr_value, weight_decay=weight_decay_value)

    raise ValueError(f"Unsupported optimizer_type: {optimizer_name}")


def get_evt_lambda_for_epoch(epoch, schedule_config, total_epochs):
    """
    Calculate EVT lambda for current epoch based on schedule.

    Args:
        epoch: Current epoch (0-indexed)
        schedule_config: Dictionary with schedule parameters
        total_epochs: Total number of training epochs

    Returns:
        lambda value for current epoch
    """
    initial = schedule_config['initial']
    mid = schedule_config['mid']
    final = schedule_config['final']
    warmup_epochs = schedule_config.get('warmup_epochs', 20)
    mid_epochs = schedule_config.get('mid_epochs', 40)
    transition = schedule_config.get('transition', 'smooth')

    if epoch < warmup_epochs:
        # Warmup phase: stay at initial lambda
        return initial
    elif epoch < mid_epochs:
        # Transition from initial to mid
        if transition == 'smooth':
            # Linear interpolation
            progress = (epoch - warmup_epochs) / (mid_epochs - warmup_epochs)
            return initial + (mid - initial) * progress
        else:  # step
            return mid
    else:
        # Transition from mid to final
        if transition == 'smooth':
            # Linear interpolation
            progress = (epoch - mid_epochs) / max(total_epochs - mid_epochs, 1)
            progress = min(progress, 1.0)  # Cap at 1.0
            return mid + (final - mid) * progress
        else:  # step
            return final


def set_global_seed(seed, deterministic=False):
    """Set random seeds for reproducible training runs."""
    seed = int(seed)
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)

    # Optional deterministic mode (slower, but more reproducible)
    if deterministic:
        torch.backends.cudnn.deterministic = True
        torch.backends.cudnn.benchmark = False


